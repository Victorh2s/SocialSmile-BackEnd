import { User } from './UserModel';
import { Posts } from './PostModel';
import { Profile } from './ProfileModel';
import { Picture } from './PictureModel';

User.hasOne(Profile, {
  foreignKey: 'profileId',
  constraints: false,
});
Profile.belongsTo(User, { foreignKey: 'profileId', constraints: false });

User.hasMany(Posts, {
  foreignKey: 'postId',
  constraints: false,
});
Posts.belongsTo(User, { foreignKey: 'postId', constraints: false });

User.hasOne(Picture, {
  foreignKey: 'pictureId',
  constraints: false,
});
Picture.belongsTo(User, { foreignKey: 'pictureId', constraints: false });
